public class Chair {
    private  int legs;
    private  String color;
    public Chair(){
        legs=0;
        color =" ";
    }
public void Setlegs(int l)
{
legs=l;
}
public void Setcolor(String c)
{
color=c;
}
 public int getlegs()
{
return legs;
}
public String getcolor()
{
return color;

}

}



